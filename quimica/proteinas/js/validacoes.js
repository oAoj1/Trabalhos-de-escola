function validar_objetivos(objetivo){
    if(objetivo.checked){
        return true
    }else{
        return false
    }
}

function validar_nome(nome){
    if(nome.length > 0){
        return true
    }else{
        return false
    }
}

function validar_idade(idade){
    if(idade.length > 0){
        return true
    }else{
        return false
    }
}

function validar_peso(peso){
    if(peso.length > 0){
        return true
    }else{
        return false
    }
}

function validar_altura(altura){
    if(altura.length > 0){
        return true
    }else{
        return false
    }
}
