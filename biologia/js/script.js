var menu = window.document.getElementById('menuabrefecha')
var secaoabrir = window.document.getElementById('entrar')
var secaofechar = window.document.getElementById('sair')

function abrir(){
    menu.style.display = 'block'
}

function fechar(){
    menu.style.display = 'none'
}

function entrar(){
    secaoabrir.style.display = 'block'
}

function sair(){
    secaofechar.style.direction = 'none'
}   